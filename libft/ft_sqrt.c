#include "libft.h"

int		ft_sqrt(int nb)
{
	int i;
	int res;

	res = 0;
	i = 0;
	if (nb == 1)
		return (1);
	while (res < nb && i < 46340)
	{
		i++;
		res = i * i;
	}
	return (i);
}
